    protected void datatable(){
        Object[] Baris ={"Kode Barang","Nama Barang","Harga Barang"};
        tabmode = new DefaultTableModel(null, Baris);
        jTable1.setModel(tabmode);
        String sql= "select * from dtbarang";
    try{
        java.sql.Statement stat=conn.createStatement();
        ResultSet hasil=stat.executeQuery(sql);
        while(hasil.next()){
            String kode=hasil.getString("kodebarang");
            String nama=hasil.getString("namabarang");
            String hrga=hasil.getString("hargabarang");
           
            String[] data={kode,nama,hrga};
            tabmode.addRow(data);
    }
    } catch (Exception e) {
        }
    }

