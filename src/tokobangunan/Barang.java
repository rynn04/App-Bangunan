    private void jSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSimpanActionPerformed
        double hrg = Double.parseDouble(jHarga.getText());
        String kd = jKode.getText();
        String n = jNama.getText();
        String sql="insert into dtbarang values(?,?,?)";
        if (jSimpan.getText().equals("Simpan")){
                sql="insert into dtbarang values(?,?,?)";}
        else {  
            sql="update dtbarang SET kodebarang=?,namabarang=?, hargabarang=? WHERE kodebarang='"+jTable1.getValueAt(jTable1.getSelectedRow(), 0)+"'";
        }
        try{
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, new kodebarang(kd).getkd());
        stat.setString(2, new namabarang(n).getnm());
        stat.setString(3, jHarga.getText());
        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Berhasil Simpan");
        jSimpan.setEnabled(false);
    } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan"+e);
        }             
    refreshform();
    }//GEN-LAST:event_jSimpanActionPerformed

